CREATE VIEW view_inventory AS
SELECT
    i.product_id,
    i.initial_stock,
    COALESCE(SUM(oi.quantity), 0) AS sold_quantity,
    i.initial_stock - COALESCE(SUM(oi.quantity), 0) AS remaining_stock
FROM inventory i
LEFT JOIN order_items oi
    ON i.product_id = oi.product_id
GROUP BY i.product_id, i.initial_stock;


CREATE TABLE Dim_Date (
    full_date DATE PRIMARY KEY,
    year INT,
    month INT,
    day INT,
    month_name VARCHAR(20),
    month_year VARCHAR(20),
    month_year_sort INT);

DECLARE @start_date DATE = (SELECT MIN(order_date) FROM orders);
DECLARE @end_date   DATE = (SELECT MAX(order_date) FROM orders);

WHILE @start_date <= @end_date
BEGIN
    INSERT INTO Dim_Date
    SELECT
        @start_date,
        YEAR(@start_date),
        MONTH(@start_date),
        DAY(@start_date),
        DATENAME(MONTH, @start_date),
        FORMAT(@start_date, 'MMM yyyy'),
        (YEAR(@start_date) * 100) + MONTH(@start_date);

    SET @start_date = DATEADD(DAY, 1, @start_date);
END;


